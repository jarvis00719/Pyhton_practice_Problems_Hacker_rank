num1 = int(input('Enter 1st number : '))
ops = input('Enter an operator {+, -, *, /}:  ')
num2 = int(input('Enter 2nd number: '))

if ops == "+":
    print(num1 + num2)

elif ops == "-":
    print(num1 - num2)

elif ops == "*":
    print(num1 * num2)

elif ops == "/":
    print(num1 / num2)

else: 
    print('Invalid Operator')









